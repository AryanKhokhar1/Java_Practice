// For each Loop

public class Chapter6_for_each_loop {
    public static void main(String[] args) {
        String[] Name = {"Aryan Khokhar", "Lavish Khokhar", "Siddhant Diweddi", "Rich Shrivastav"};

        // It's For each loop
        for(String element : Name){
            System.out.println(element);
        }
    }
}
